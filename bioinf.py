
def scan(sequence):
    index = 0; result = []
    while index < len(sequence):
        string = ""
        index_st = sequence[index:].find("ATG") + index
        while index_st in range(len(sequence)):
            next_codon = sequence[index_st:index_st+3]
            string += next_codon
            index_st +=3
            if (next_codon == "TAA") or (next_codon == "TGA") or (next_codon == "TAG"):
                result.append(string)
                index += index_st
                break
    return result

def translate(reading_frames, aminos):
    result = []
    for index in range(len(reading_frames)):
        string = ""
        lst = [reading_frames[index][i:i+3] for i in range(0,len(reading_frames[index]),3)]
        for k in range(len(lst)):
            for i, j in aminos.items():
                lst[k] = lst[k].replace(i, j)
            string += lst[k]
        result.append(string)
    return result

